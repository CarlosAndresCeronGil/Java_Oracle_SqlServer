    private void oracleConectarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_oracleConectarButtonActionPerformed
        // TODO add your handling code here:
        MetodosDB.setEsOracle(true);
        MetodosDB.setEsSQLServer(false);
        MetodosDB.setUsuario(usuarioTextField.getText());
        MetodosDB.setContrasena(new String(contrasenaTextField.getPassword()));
        MetodosDB.setInstancia(instanciaTextField.getText());
        MetodosDB.setIpServidor(ipServidorTextField.getText());
        MetodosDB.setPuerto(puertoTextField.getText());
        if(MetodosDB.getConnection() != null){
            Principal principal = new Principal();
            
            this.setVisible(false);
            principal.setLocationRelativeTo(null);
            principal.setResizable(false);
            principal.setVisible(true);
        }
    }//GEN-LAST:event_oracleConectarButtonActionPerformed

