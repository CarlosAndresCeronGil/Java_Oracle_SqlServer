    private void motorBDComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_motorBDComboBoxActionPerformed
        // TODO add your handling code here:
        if(motorBDComboBox.getSelectedItem().equals("Oracle")){
            //setEsOracle(true);
            //setEsSQLServer(false);
            MetodosDB.setEsOracle(true);
            MetodosDB.setEsSQLServer(false);
            motorTabbedPanel.setSelectedIndex(0);
        }else if(motorBDComboBox.getSelectedItem().equals("SQL Server")){
            MetodosDB.setEsOracle(false);
            MetodosDB.setEsSQLServer(true);
            motorTabbedPanel.setSelectedIndex(1);
        }
    }//GEN-LAST:event_motorBDComboBoxActionPerformed

